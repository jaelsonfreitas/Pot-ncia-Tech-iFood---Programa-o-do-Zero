// estrutura de decisão 
// switch/case/break/default

let fruta = "Melão"

switch(fruta){
	case "Laranja": 
    console.log("Suco de Laranja")
    break
    
    case "Banana":
    console.log("Vitamina de Banana")
    break
    
    case "Maça":
    console.log("Suco de maça")
    break
    
    default:
    console.log("Suco genérico")
}